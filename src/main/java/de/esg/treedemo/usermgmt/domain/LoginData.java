package de.esg.treedemo.usermgmt.domain;

public class LoginData
{
	private String user;
	private String passwd;

	public String getUser()
	{
		return this.user;
	}

	public void setUser(String user)
	{
		this.user = user;
	}

	public String getPasswd()
	{
		return this.passwd;
	}

	public void setPasswd(String passwd)
	{
		this.passwd = passwd;
	}

}
